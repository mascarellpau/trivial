function changeColor(x)
{
    if(x.style.background=="rgb(250, 0, 0)")
    {
        x.style.opacity="0";
        x.style.background="#fff";
    }else{
        x.style.background="rgb(250, 0, 0)";
        x.style.opacity="1";
    }
    return false;
}

