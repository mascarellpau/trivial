function changeColorRoig(x)
{
    if(x.style.background=="rgb(229, 38, 53)")
    {
        x.style.opacity="0";
        x.style.background="#fff";
    }else{
        x.style.background="rgb(229, 38, 53)";
        x.style.opacity="1";
    }
    return false;
}

